package Store;

public class Laptop {
    public String brand;
    public String model;
    public byte ram;
    public short memory;
    public String operationSystem;
    public String color;
    public int id;

    public Laptop(String brand, String model, byte ram, short memory, String operationSystem, String color, int id) {
        this.brand = brand;
        this.model = model;
        this.ram = ram;
        this.memory = memory;
        this.operationSystem = operationSystem;
        this.color = color;
        this.id = id;
    }






    

}
